package com.obapps.schoolchatbot.core.models;

/**
 * Enum representing different types of policies.
 * This enum is used to categorize policies into three types:
 * 1. School: Represents school policies.
 * 2. StateLaw: Represents state laws.
 * 3. FederalLaw: Represents federal laws.
 */
public enum PolicyType {
  /**
   * Represents school policies.
   */
  School,
  /**
   * Represents state laws.
   */
  StateLaw,
  /**
   * Represents federal laws.
   */
  FederalLaw,
}
